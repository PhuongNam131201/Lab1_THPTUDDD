fun main() {
    println("Nhập a: ")
    val a = readLine()!!.toInt()
    println("Nhập b: ")
    val b = readLine()!!.toInt()

    // So sánh hai số
    println("a == b: ${a == b}")
    println("a != b: ${a != b}")
    println("a > b: ${a > b}")
    println("a < b: ${a < b}")
    println("a >= b: ${a >= b}")
    println("a <= b: ${a <= b}")
    println("test : ${a<b}")
}
